package tech.cae.cauldron.api;

/**
 *
 * @author peter
 */
public class CauldronConfiguration {

    private final String dbHost;
    private final int dbPort;
    private final String dbName;
    private final String dbCollection;

    public CauldronConfiguration(String dbHost, int dbPort, String dbName, String dbCollection) {
        this.dbHost = dbHost;
        this.dbPort = dbPort;
        this.dbName = dbName;
        this.dbCollection = dbCollection;
    }

    public String getDbHost() {
        return dbHost;
    }

    public int getDbPort() {
        return dbPort;
    }

    public String getDbName() {
        return dbName;
    }

    public String getDbCollection() {
        return dbCollection;
    }

}
